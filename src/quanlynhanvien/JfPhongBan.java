private void xuatExcel() {
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("L∆∞u file Excel");
    fileChooser.setSelectedFile(new File("cayphongban.xlsx"));
    int userSelection = fileChooser.showSaveDialog(this);

    if (userSelection == JFileChooser.APPROVE_OPTION) {
        File fileToSave = fileChooser.getSelectedFile();
        if (!fileToSave.getName().toLowerCase().endsWith(".xlsx")) {
            fileToSave = new File(fileToSave.getAbsolutePath() + ".xlsx");
        }

        try (Workbook workbook = new XSSFWorkbook();
             FileOutputStream fileOut = new FileOutputStream(fileToSave)) {

            Sheet sheet = workbook.createSheet("PhongBan_NhanVien");

            DefaultMutableTreeNode root = (DefaultMutableTreeNode) jTree1.getModel().getRoot();
            int[] rowIndex = {0};
            traverseTreeForExcel(root, sheet, 0, rowIndex);

            workbook.write(fileOut);
            ThongBao("Xu·∫•t Excel th√†nh c√¥ng!", "Th√¥ng b√°o", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            ex.printStackTrace();
            ThongBao("L·ªói khi xu·∫•t Excel: " + ex.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
        }
    }
}

private void traverseTreeForExcel(DefaultMutableTreeNode node, Sheet sheet, int level, int[] rowIndex) {
    Row row = sheet.createRow(rowIndex[0]++);
    Cell cell = row.createCell(level);
    Object obj = node.getUserObject();

    // Ghi ƒë√∫ng th√¥ng tin
    if (obj instanceof PhongBan pb) {
        cell.setCellValue("Ph√≤ng ban: " + pb.getTenPB());
    } else if (obj instanceof NhanVien nv) {
        cell.setCellValue("Nh√¢n vi√™n: " + nv.getHoTen());
    } else {
        cell.setCellValue(node.toString());
    }

    for (int i = 0; i < node.getChildCount(); i++) {
        traverseTreeForExcel((DefaultMutableTreeNode) node.getChildAt(i), sheet, level + 1, rowIndex);
    }
}
private void xuatPDF() {
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("L∆∞u file PDF");
    fileChooser.setSelectedFile(new File("cayphongban.pdf"));
    int userSelection = fileChooser.showSaveDialog(this);

    if (userSelection == JFileChooser.APPROVE_OPTION) {
        File fileToSave = fileChooser.getSelectedFile();
        if (!fileToSave.getName().toLowerCase().endsWith(".pdf")) {
            fileToSave = new File(fileToSave.getAbsolutePath() + ".pdf");
        }

        try (FileOutputStream fos = new FileOutputStream(fileToSave)) {
            Document document = new Document();
            PdfWriter.getInstance(document, fos);
            document.open();

            document.add(new Paragraph("DANH S√ÅCH PH√íNG BAN V√Ä NH√ÇN VI√äN",
                    FontFactory.getFont(FontFactory.HELVETICA_BOLD, 16)));
            document.add(new Paragraph(" "));

            DefaultMutableTreeNode root = (DefaultMutableTreeNode) jTree1.getModel().getRoot();
            traverseTreeForPDF(root, document, 0);

            document.close();
            ThongBao("Xu·∫•t PDF th√†nh c√¥ng!", "Th√¥ng b√°o", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            ex.printStackTrace();
            ThongBao("L·ªói khi xu·∫•t PDF: " + ex.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
        }
    }
}

private void traverseTreeForPDF(DefaultMutableTreeNode node, Document document, int level) throws DocumentException {
    String indent = "    ".repeat(level);
    Object obj = node.getUserObject();
    String line;

    if (obj instanceof PhongBan pb) {
        line = indent + "üè¢ Ph√≤ng ban: " + pb.getTenPB();
    } else if (obj instanceof NhanVien nv) {
        line = indent + "üë§ Nh√¢n vi√™n: " + nv.getHoTen();
    } else {
        line = indent + node.toString();
    }

    document.add(new Paragraph(line));

    for (int i = 0; i < node.getChildCount(); i++) {
        traverseTreeForPDF((DefaultMutableTreeNode) node.getChildAt(i), document, level + 1);
    }
}


